package com.kodilla.basic_assertion;

public class Calculator {

    public int sum(int a, int b) {
        return a + b;
    }

    public int subtract(int a, int b) {
        return a - b;
    }
    public int squaredA(int a) {
        return a * a;
    }
    public int squaredB(int b) {
        return b * b;
    }


}